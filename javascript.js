function mobileMenu(){

    var e = document.getElementById("myMenu");
    
    if (e.style.visibility === "hidden"){
        document.getElementById("myMenu").style.visibility = "visible";
        
    }
    else {
        document.getElementById("myMenu").style.visibility = "hidden";
        
    }
}


function mobileMenu2(){

    var z = document.getElementsByClassName("Req-main");
    
    if (z.style.display === "none"){
        document.getElementsByClassName("Req-main").style.display = "block";
        
    }
    else {
        document.getElementsByClassName("Req-main").style.display = "none";
        
    }
}



